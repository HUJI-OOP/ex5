package ex5.parser;
/**
 * An enumeration representing different types of lines in a simplified Java-like language.
 * @author Eilam Soroka, Maayan Felig
 */
public enum LineType {
    METHOD_DECLARATION,
    IF_WHILE_CONDITION,
    VARIABLE_DECLARATION,
    ASSIGNMENT,
    METHOD_CALL,
    RETURN,
    SCOPE_CLOSE,
}
